package br.edu.insper.desagil.aula6.rpg;

public class Elemento {
	private int x;
	private int y;

	public Elemento(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
